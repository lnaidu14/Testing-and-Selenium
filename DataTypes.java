package javaBasics;

public class DataTypes {

	public static void main(String[] args) {
		
		// Primitive data types: int, double, char, boolean
		
		
		// 1. int -> Any integer value
		int i = 10;
		System.out.println(i);
		
		//typing syso and then pressing CTRL+Space will write the print statement
		
		//2.double -> Decimal number
		double d = 12.33;
		System.out.println(d);
		
		//3.char ->Single character value which is written within single quotes
		char c = 'B';
		char b = 'C';
		char t = 'c';
		
		//4. boolean
		boolean b1 = true;
		boolean b2 = false;
		
		//5. String -> This is a class but is used as a data type
		String s ="Hello World";
		String s1 = "Selenium";
		String s2 = "Hi this is my first java code";
		System.out.println(s);
		System.out.println(s2);

	}

}
